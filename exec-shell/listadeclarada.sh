#!/bin/bash
#####################################################################
# Program:
# Description:
# Ler valores de uma lista, declarada na estrutura de repeticao - for
# Author: Rodrigo Morais <rodrigo.morais@concretesolutions.com.br>
#####################################################################
for valor in salgado esfiha 'pão de queijo' 'pão doce' 'pão com gergelim'
do
	echo GostoMuito $valor
# vai ser exibido em cada linha GostoMuito salgado, GostoMuito esfiha, GostoMuito pão de queijo, GostoMuito pão com gergelim
done
